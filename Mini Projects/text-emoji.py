#Text-Emoji Converter

print("Welcome to text-emoji converter!!!")

t_e = input("What text would you like to convert to an emoji? ")

if t_e == "turtle":
    print("🐢")

elif t_e == "smiling face":
    print("😀")

elif t_e == "tiger":
    print("🐅")

else:
    print("Incorrect Syntax OR That Emoji Is Not Currently In Our Library ")
